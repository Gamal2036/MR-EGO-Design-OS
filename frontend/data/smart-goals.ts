function createMilestone(
  id: string,
  goalId: string,
  order: number,
  title: string,
  description: string,
  weight: number,
  status: "not_started" | "in_progress" | "completed" | "skipped" = "not_started",
  dueOffsetWeeks = 0
) {
  return {
    id,
    goalId,
    title,
    description,
    status,
    order,
    weight,
    dueDate: dueOffsetWeeks > 0 ? weeksFromNow(dueOffsetWeeks) : undefined,
    completedAt: status === "completed" ? daysAgo(order) : undefined,
  };
}

function createTask(
  id: string,
  goalId: string,
  order: number,
  title: string,
  description: string,
  status: "todo" | "in_progress" | "done" | "skipped" = "todo",
  priority: "low" | "medium" | "high" | "critical" = "medium",
  milestoneId?: string,
  dueOffsetDays = 0
) {
  return {
    id,
    goalId,
    milestoneId,
    title,
    description,
    status,
    priority,
    dueDate: dueOffsetDays > 0 ? daysFromNow(dueOffsetDays) : undefined,
    completedAt: status === "done" ? daysAgo(order) : undefined,
    estimatedMinutes: 30 + order * 15,
    order,
  };
}

