const now = Date.now();
const MS_DAY = 86400000;
const MS_HOUR = 3600000;

function daysAgo(d: number): string {
  return new Date(now - d * MS_DAY).toISOString();
}

function daysFromNow(d: number): string {
  return new Date(now + d * MS_DAY).toISOString();
}

